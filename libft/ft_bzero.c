#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*ptr;
	unsigned int	i;

	i = 0;
	ptr = s;
	while (n > i)
	{
		ptr[i] = '\0';
		i++;
	}
}
